// Helper functions
export function getPostBySlug(slug: string): BlogPostMeta | undefined {
  return blogPostsMeta.find(post => post.slug === slug)
}

export function getPostsByGame(game: string): BlogPostMeta[] {
  if (game === 'All Games') return blogPostsMeta
  return blogPostsMeta.filter(post => post.game === game)
}

export function getPostsByCategory(category: string): BlogPostMeta[] {
  if (category === 'All') return blogPostsMeta
  return blogPostsMeta.filter(post => post.category === category)
}

export function getRelatedPosts(currentSlug: string, limit: number = 3): BlogPostMeta[] {
  const currentPost = getPostBySlug(currentSlug)
  if (!currentPost) return blogPostsMeta.slice(0, limit)
  
  // Find posts with same game or category, excluding current
  return blogPostsMeta
    .filter(post => post.slug !== currentSlug)
    .filter(post => post.game === currentPost.game || post.category === currentPost.category)
    .slice(0, limit)
}

export function getAllSlugs(): string[] {
  return blogPostsMeta.map(post => post.slug)
}

